package Day9;

public class ShorthandOperators {

	public static void main(String[] args) {
		// addition
		int apples =5;
		apples +=2;
		System.out.println(apples);
		apples = apples+3; // this is shorthand way : apples +=3;
		System.out.println(apples);
		
		// multiply by two
		 apples *=2; //  apples = apples*2;
		System.out.println(apples);
		
		apples /=2;
		System.out.println(apples);
		
	}

}
